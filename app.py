def to_decimal(value):
    return Decimal(str(value)).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)

def money(value):
    return f"₹{to_decimal(value):,.2f}"

def safe_str(value):
    return str(value).strip() if value is not None else ""

def format_date(dt_value):
    if isinstance(dt_value, datetime):
        return dt_value.strftime("%d-%m-%Y")
    try:
        return datetime.strptime(str(dt_value), "%Y-%m-%d %H:%M:%S").strftime("%d-%m-%Y")
    except Exception:
        return str(dt_value)

def validate_invoice_payload(data):
    required_fields = [
        "invoice_no",
        "customer_name",
        "subtotal",
        "gst_percent",
        "gst_amount",
        "grand_total",
        "items"
    ]

    for field in required_fields:
        if field not in data:
            return f"Missing required field: {field}"

    if not isinstance(data["items"], list) or len(data["items"]) == 0:
        return "Invoice must contain at least one item"

    for i, item in enumerate(data["items"], start=1):
        for key in ["description", "quantity", "rate", "amount"]:
            if key not in item:
                return f"Missing '{key}' in item {i}"

    return None

def fetch_invoice(invoice_id):
    connection = get_db_connection()
    try:
        with connection.cursor() as cursor:
            cursor.execute("SELECT * FROM invoices WHERE id = %s", (invoice_id,))
            invoice = cursor.fetchone()

            if not invoice:
                return None, None

            cursor.execute(
                "SELECT * FROM invoice_items WHERE invoice_id = %s ORDER BY id ASC",
                (invoice_id,)
            )
            items = cursor.fetchall()
            return invoice, items
    finally:
        connection.close()

