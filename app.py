@app.route("/save_invoice", methods=["POST"])
def save_invoice():
    data = request.get_json(silent=True)

    if not data:
        return jsonify({"error": "Invalid or empty JSON payload"}), 400

    validation_error = validate_invoice_payload(data)
    if validation_error:
        return jsonify({"error": validation_error}), 400

    connection = None
    try:
        connection = get_db_connection()

        with connection.cursor() as cursor:
            invoice_date_obj = datetime.now()

            cursor.execute("""
                INSERT INTO invoices
                (invoice_no, customer_name, customer_email, invoice_date, subtotal, gst_percent, gst_amount, grand_total)
                VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
            """, (
                safe_str(data["invoice_no"]),
                safe_str(data["customer_name"]),
                safe_str(data.get("customer_email", "")),
                invoice_date_obj,
                to_decimal(data["subtotal"]),
                to_decimal(data["gst_percent"]),
                to_decimal(data["gst_amount"]),
                to_decimal(data["grand_total"])
            ))

            invoice_id = cursor.lastrowid

            item_rows = []
            for item in data["items"]:
                item_rows.append((
                    invoice_id,
                    safe_str(item["description"]),
                    int(item["quantity"]),
                    to_decimal(item["rate"]),
                    to_decimal(item["amount"])
                ))

            cursor.executemany("""
                INSERT INTO invoice_items (invoice_id, description, quantity, rate, amount)
                VALUES (%s, %s, %s, %s, %s)
            """, item_rows)

        connection.commit()

        return jsonify({
            "message": "Invoice saved successfully",
            "invoice_id": invoice_id
        }), 201

    except Exception as e:
        if connection:
            connection.rollback()
        return jsonify({"error": str(e)}), 500

    finally:
        if connection:
            connection.close()

