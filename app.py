def draw_box(c, x, y, w, h, title, lines):
    c.setStrokeColor(colors.HexColor("#CBD5E1"))
    c.setFillColor(colors.white)
    c.roundRect(x, y - h, w, h, 8, stroke=1, fill=1)

    c.setFillColor(colors.HexColor("#0F172A"))
    c.setFont("Helvetica-Bold", 11)
    c.drawString(x + 10, y - 18, title)

    text_y = y - 36
    c.setFont("Helvetica", 10)
    for line in lines:
        c.drawString(x + 10, text_y, safe_str(line))
        text_y -= 14

def draw_table_header(c, y):
    c.setFillColor(colors.HexColor("#E2E8F0"))
    c.rect(40, y - 18, 515, 20, stroke=0, fill=1)

    c.setFillColor(colors.HexColor("#0F172A"))
    c.setFont("Helvetica-Bold", 10)
    c.drawString(45, y - 5, "S.No")
    c.drawString(80, y - 5, "Description")
    c.drawString(300, y - 5, "Qty")
    c.drawString(380, y - 5, "Rate (₹)")
    c.drawString(470, y - 5, "Amount (₹)")

def draw_item_row(c, item, index, y):
    c.setFillColor(colors.black)
    c.setFont("Helvetica", 10)

    row_top = y
    c.drawString(45, row_top, str(index))

    description = safe_str(item.get("description", ""))
    desc_lines = textwrap.wrap(description, width=36) or [""]
    desc_y = row_top
    for line in desc_lines:
        c.drawString(80, desc_y, line)
        desc_y -= 12

    c.drawString(300, row_top, safe_str(item.get("quantity", "")))
    c.drawRightString(455, row_top, f"{to_decimal(item.get('rate', 0)):,.2f}")
    c.drawRightString(545, row_top, f"{to_decimal(item.get('amount', 0)):,.2f}")

    row_height = max(18, len(desc_lines) * 12)
    c.setStrokeColor(colors.HexColor("#E2E8F0"))
    c.line(40, row_top - row_height + 4, 555, row_top - row_height + 4)

    return y - row_height

def draw_summary(c, x, y, invoice, cgst_amount, sgst_amount):
    box_w = 220
    box_h = 90

    c.setStrokeColor(colors.HexColor("#CBD5E1"))
    c.roundRect(x, y - box_h, box_w, box_h, 8, stroke=1, fill=0)

    c.setFont("Helvetica", 10)
    c.drawString(x + 12, y - 20, "Subtotal:")
    c.drawRightString(x + box_w - 12, y - 20, money(invoice["subtotal"]))

    half_gst = to_decimal(invoice["gst_percent"]) / 2
    c.drawString(x + 12, y - 40, f"CGST ({half_gst}%):")
    c.drawRightString(x + box_w - 12, y - 40, money(cgst_amount))

    c.drawString(x + 12, y - 60, f"SGST ({half_gst}%):")
    c.drawRightString(x + box_w - 12, y - 60, money(sgst_amount))

    c.setStrokeColor(colors.HexColor("#94A3B8"))
    c.line(x + 10, y - 70, x + box_w - 10, y - 70)

    c.setFont("Helvetica-Bold", 11)
    c.drawString(x + 12, y - 84, "Total Amount:")
    c.drawRightString(x + box_w - 12, y - 84, money(invoice["grand_total"]))

def generate_pdf(invoice_id, customer_phone="", customer_address="Customer Address"):
    invoice, items = fetch_invoice(invoice_id)
    if not invoice:
        return None

    pdf_path = os.path.join(PDF_FOLDER, f"invoice_{invoice_id}.pdf")
    c = canvas.Canvas(pdf_path, pagesize=A4)
    width, height = A4

    draw_header(c, width, height)

    y = height - 125
    draw_box(
        c, 40, y, 245, 80, "Bill To", [
            invoice["customer_name"],
            customer_address,
            f"Phone: {customer_phone or 'N/A'}"
        ]
    )

    invoice_date = invoice["invoice_date"]
    due_date_text = "Within 7 days"
    try:
        if isinstance(invoice_date, datetime):
            due_date_text = (invoice_date + timedelta(days=7)).strftime("%d-%m-%Y")
        else:
            parsed = datetime.strptime(str(invoice_date), "%Y-%m-%d %H:%M:%S")
            due_date_text = (parsed + timedelta(days=7)).strftime("%d-%m-%Y")
    except Exception:
        pass

    draw_box(
        c, 310, y, 245, 80, "Invoice Info", [
            f"Invoice Number: {invoice['invoice_no']}",
            f"Invoice Date: {format_date(invoice['invoice_date'])}",
            f"Due Date: {due_date_text}"
        ]
    )

    y = height - 250
    c.setFillColor(colors.HexColor("#0F172A"))
    c.setFont("Helvetica-Bold", 12)
    c.drawString(40, y, "Item Details")
    y -= 18

    draw_table_header(c, y)
    y -= 30

    for index, item in enumerate(items, start=1):
        if y < 170:
            draw_footer(c, width)
            c.showPage()
            draw_header(c, width, height)
            y = height - 120
            draw_table_header(c, y)
            y -= 30
        y = draw_item_row(c, item, index, y)

    gst_total = to_decimal(invoice["gst_amount"])
    cgst_amount = (gst_total / Decimal("2")).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    sgst_amount = (gst_total / Decimal("2")).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)

    y -= 20
    draw_summary(c, 335, y, invoice, cgst_amount, sgst_amount)

    y -= 115
    draw_box(c, 40, y, 250, 90, "Payment Details", [
        f"Bank Name: {BUSINESS_INFO['bank_name']}",
        f"Account No: {BUSINESS_INFO['account_no']}",
        f"IFSC Code: {BUSINESS_INFO['ifsc']}",
        f"UPI ID: {BUSINESS_INFO['upi_id']}"
    ])

    draw_box(c, 305, y, 250, 90, "Terms & Conditions", [
        "Payment due within 7 days",
        "Late payment may incur charges",
        "Goods once sold will not be returned"
    ])

    y -= 120
    c.setFont("Helvetica-Bold", 10)
    c.drawRightString(width - 50, y, "Authorized Signature")
    c.setFont("Helvetica", 10)
    c.drawRightString(width - 50, y - 16, f"({BUSINESS_INFO['signature_name']})")

    draw_footer(c, width)
    c.save()
    return pdf_path

