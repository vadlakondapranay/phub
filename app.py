def send_invoice_email(to_email, pdf_path, invoice, items):
    if not to_email:
        return "Email skipped (no customer email)"

    msg = EmailMessage()
    msg["Subject"] = f"Your Invoice {invoice['invoice_no']} from {BUSINESS_INFO['name']}"
    msg["From"] = EMAIL_CONFIG["sender_email"]
    msg["To"] = to_email

    body = f"""
Hello {invoice['customer_name']},

Thank you for your purchase.

Invoice Number: {invoice['invoice_no']}
Invoice Date: {format_date(invoice['invoice_date'])}
Total Amount: {money(invoice['grand_total'])}

Please find your invoice PDF attached.

Regards,
{BUSINESS_INFO['name']}
"""
    msg.set_content(body.strip())

    with open(pdf_path, "rb") as f:
        pdf_data = f.read()
        msg.add_attachment(
            pdf_data,
            maintype="application",
            subtype="pdf",
            filename=os.path.basename(pdf_path)
        )

    try:
        with smtplib.SMTP(EMAIL_CONFIG["smtp_server"], EMAIL_CONFIG["smtp_port"]) as server:
            server.starttls()
            server.login(EMAIL_CONFIG["sender_email"], EMAIL_CONFIG["sender_password"])
            server.send_message(msg)
        return "Email sent successfully"
    except Exception as e:
        return f"Email failed: {str(e)}"

def build_sms_message(invoice, items):
    item_count = len(items)
    return (
        f"{BUSINESS_INFO['name']}: Invoice {invoice['invoice_no']} generated. "
        f"Items: {item_count}, Total: {money(invoice['grand_total'])}, "
        f"Date: {format_date(invoice['invoice_date'])}. Thank you for your purchase."
    )

def send_sms(to_phone, message):
    if not to_phone:
        return "SMS skipped (no phone number)"
    if not SMS_CONFIG["enabled"]:
        return "SMS skipped (service disabled)"
    if not TWILIO_AVAILABLE:
        return "SMS skipped (twilio package not installed)"

    try:
        client = Client(SMS_CONFIG["account_sid"], SMS_CONFIG["auth_token"])
        client.messages.create(
            body=message,
            from_=SMS_CONFIG["from_number"],
            to=to_phone
        )
        return "SMS sent"
    except Exception as e:
        return f"SMS failed: {str(e)}"

