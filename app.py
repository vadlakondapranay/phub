@app.route("/send_invoice/<int:invoice_id>", methods=["POST"])
def send_invoice(invoice_id):
    try:
        data = request.get_json(silent=True) or {}
        customer_phone = safe_str(data.get("customer_phone", ""))
        customer_address = safe_str(data.get("customer_address", "Customer Address"))

        invoice, items = fetch_invoice(invoice_id)
        if not invoice:
            return jsonify({"error": "Invoice not found"}), 404

        pdf_path = generate_pdf(invoice_id, customer_phone, customer_address)
        if not pdf_path or not os.path.exists(pdf_path):
            return jsonify({"error": "Failed to generate PDF"}), 500

        email_status = send_invoice_email(
            invoice.get("customer_email", ""),
            pdf_path,
            invoice,
            items
        )

        sms_status = send_sms(
            customer_phone,
            build_sms_message(invoice, items)
        )

        return jsonify({
            "message": "Invoice sending completed",
            "email_status": email_status,
            "sms_status": sms_status
        }), 200

    except Exception as e:
        return jsonify({"error": str(e)}), 500

